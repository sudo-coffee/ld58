local class = require("class")
local groups = {}

-- \ ---- \ -------------------------------------------------------------- \ --
-- | room | -------------------------------------------------------------- | --
-- \ ---- \ -------------------------------------------------------------- \ --

function groups.room(world)
  local this = {}
  local super = class.newGroup(world)
  setmetatable(this, { __index = super })

  this.name = "room"

  function this:draw(pass)
    super:draw(pass)
  end

  return this
end

-- \ --------------------------------------------------------------------- \ --
-- | --------------------------------------------------------------------- | --
-- \ --------------------------------------------------------------------- \ --

return groups
