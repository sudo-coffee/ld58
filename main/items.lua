local class = require("class")
local items = {}

-- \ ---- \ -------------------------------------------------------------- \ --
-- | base | -------------------------------------------------------------- | --
-- \ ---- \ -------------------------------------------------------------- \ --

function items.base(world)
  local this = {}
  local super = class.newItem(world)
  setmetatable(this, { __index = super })

  this.filter = { "room" }

  return this
end

-- \ --------------------------------------------------------------------- \ --
-- | --------------------------------------------------------------------- | --
-- \ --------------------------------------------------------------------- \ --

return items
