local class = require("class")
local items = {}

-- \ ------ \ ------------------------------------------------------------ \ --
-- | Player | ------------------------------------------------------------ | --
-- \ ------ \ ------------------------------------------------------------ \ --

function items.player()
  local this = {}
  local super = class.newItem()
  setmetatable(this, { __index = super })

  this.groups = { "base" }
  this.enabled = true -- player always starts enabled

  return this
end

-- \ ---- \ -------------------------------------------------------------- \ --
-- | Test | -------------------------------------------------------------- | --
-- \ ---- \ -------------------------------------------------------------- \ --

function items.test()
  local this = {}
  local super = class.newItem()
  setmetatable(this, { __index = super })

  this.groups = { "two" }

  return this
end

-- \ --------------------------------------------------------------------- \ --
-- | --------------------------------------------------------------------- | --
-- \ --------------------------------------------------------------------- \ --

return items
