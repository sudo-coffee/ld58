local ffi = require("ffi")
local display = {}

-- \ ------ \ ------------------------------------------------------------ \ --
-- | c defs | ------------------------------------------------------------ | --
-- \ ------ \ ------------------------------------------------------------ \ --

local disp = ffi.load('display')
ffi.cdef([[
  void init();
  int getWidth();
  int getHeight();
]])

-- \ ---- \ -------------------------------------------------------------- \ --
-- | init | -------------------------------------------------------------- | --
-- \ ---- \ -------------------------------------------------------------- \ --

disp.init()

-- \ --------- \ --------------------------------------------------------- \ --
-- | functions | --------------------------------------------------------- | --
-- \ --------- \ --------------------------------------------------------- \ --

function display.getWidth()
  return disp.getWidth()
end

function display.getHeight()
  return disp.getHeight()
end

-- \ --------------------------------------------------------------------- \ --
-- | --------------------------------------------------------------------- | --
-- \ --------------------------------------------------------------------- \ --

return display
